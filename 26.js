let num1=parseFloat(prompt("Enter first number:"))
let num2=parseFloat(prompt("Enter second number:"))
let num=parseFloat(prompt("press"))
switch (num){
    case '+':
    console.log(num1+num2)
    break;
    case '-':
    console.log(num1-num2)
    break;
    case '*':
    console.log(num1*num2)
    break;
    case '/':
    console.log(num1/num2)
    break;

}